    # 找 extra:[ 的开始
    extra_pos = start + len(pattern)
    
    # 找到第一个 extra:[
    first_extra = html.find("extra:[", start)
    if first_extra == -1:
        print(f"错误：未找到 {province_name} 的 extra:[")
        return html, False
    
    # 找到 extra:] 的位置
    bracket_count = 0
    i = first_extra + len("extra:[")
    in_extra = False
    extra_end = -1
    while i < len(html):
        c = html[i]
        if c == '[':
            bracket_count += 1
            in_extra = True
        elif c == ']':
            if not in_extra:
                extra_end = i + 1
                break
            elif bracket_count == 0:
                extra_end = i + 1
                break
            else:
                bracket_count -= 1
        elif c == '\\' and i + 1 < len(html) and html[i+1] == 'n':
            pass
        i += 1
    
    if extra_end == -1:
        # 尝试更简单的方法
        extra_end = html.find("],", first_extra)
        if extra_end != -1:
            extra_end += 2
    
    # 找 questions:[
    questions_pos = html.find("questions:[", extra_end)
    if questions_pos == -1:
        print(f"错误：未找到 {province_name} 的 questions:[")
        return html, False
    
    # 找 questions:] 的位置
    qbracket_count = 0
    j = questions_pos + len("questions:[")
    in_q = False
    questions_end = -1
    while j < len(html):
        c = html[j]
        if c == '[':
            qbracket_count += 1
            in_q = True
        elif c == ']':
            if not in_q:
                questions_end = j + 1
                break
            elif qbracket_count == 0:
                questions_end = j + 1
                break
            else:
                qbracket_count -= 1
        j += 1
    
    if questions_end == -1:
        questions_end = html.find("],", questions_pos)
        if questions_end != -1:
            questions_end += 2
    
    # 找块结束 - 找 }, 后面跟着另一个省份
    block_end = html.find("},", questions_end)
    if block_end == -1:
        block_end = questions_end + 100
    
    # 保留 region 和 official
    region_line_end = html.find(",\\nofficial:", start)
    if region_line_end == -1:
        region_line_end = html.find(",\\nofficial:", start)
    
    official_start = html.find("official:", start)
    official_end = html.find("},", official_start)
    if official_end == -1 or official_end > first_extra:
        official_end = html.find("},", first_extra)
    
    official_block = html[official_start:official_end+2]
    
    # 构建新块
    new_block = f"{province_name}:{{\\nregion:'{region}',\\n{official_block}\\nextra:{new_extra},\\nquestions:{new_questions}\\n}}"
    
    # 替换
    old_block = html[start:block_end]
    html = html[:start] + new_block + html[block_end:]
    
    return html, True

