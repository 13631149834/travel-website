def esc(s):
    """转义JS字符串中的特殊字符"""
    if not isinstance(s, str):
        s = str(s)
    return s.replace('\\', '\\\\').replace("'", "\\'").replace('\n', ' ')

def find_block_end(html, start):
    """使用花括号计数器定位JS对象块结束位置"""
    brace_count = 0
    in_str = False
    str_char = None
    i = start
    while i < len(html):
        c = html[i]
        if in_str:
            if c == str_char and (i == 0 or html[i-1] != '\\'):
                in_str = False
        else:
            if c in ("'", '"'):
                in_str = True
                str_char = c
            elif c == '{':
                brace_count += 1
            elif c == '}':
                brace_count -= 1
                if brace_count == 0:
                    return i + 1
        i += 1
    return len(html)

def count_chinese_chars(text):
    """计算中文字符数量"""
    return len(re.findall(r'[\u4e00-\u9fff]', text))

