def generate_extra_js(province_data):
    """生成extra数组的JS代码"""
    items_template = []
    for item in province_data["items"]:
        items_template.append("          '" + esc(item) + "'")
    items_str = ",\n".join(items_template)
    return f"""[
      {{
        "title": "{province_data["title"]}",
        "items": [
{items_str}
        ]
      }}"""

def generate_all_extra(extra_list):
    """生成完整的extra数组"""
    parts = []
    for i, extra in enumerate(extra_list):
        items_parts = []
        for item in extra["items"]:
            items_parts.append("          '" + esc(item) + "'")
        items_str = ",\n".join(items_parts)
        comma = "," if i < len(extra_list) - 1 else ""
        parts.append(f"""      {{
        "title": "{extra["title"]}",
        "items": [
{items_str}
        ]
      }}{comma}""")
    return "[\n" + ",\n\n".join(parts) + "\n    ]"

def process_province(html_content, province_name, province_data):
    """处理单个省份的数据替换"""
    # 查找省份起始位置
    start_marker = f'  "{province_name}": {{'
    start_idx = html_content.find(start_marker)
    if start_idx == -1:
        print(f"未找到省份 {province_name}")
        return html_content, False
    
    # 查找 extra 数组的起始位置
    extra_start_marker = '    "extra": ['
    extra_start_idx = html_content.find(extra_start_marker, start_idx)
    if extra_start_idx == -1:
        print(f"未找到省份 {province_name} 的 extra 数组")
        return html_content, False
    
    # 找到第一个 { 的位置
    brace_start = html_content.find('{', extra_start_idx)
    if brace_start == -1:
        print(f"未找到 extra 数组的起始花括号")
        return html_content, False
    
    # 使用花括号计数器定位 extra 数组结束位置
    extra_end_idx = find_block_end(html_content, brace_start)
    
    # 生成新的 extra 数组
    new_extra = "    \"extra\": " + generate_all_extra(province_data["extra"]) + ","
    
    # 替换
    new_content = html_content[:extra_start_idx] + new_extra + html_content[extra_end_idx:]
    
    return new_content, True

