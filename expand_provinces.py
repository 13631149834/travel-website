def replace_province_block(html, province_name, items1, items2, items3, items4, items5):
    """替换省份的extra部分"""
    # 找到省份块的起始位置
    pos = html.find(f'"{province_name}": {{')
    if pos < 0:
        print(f"未找到省份: {province_name}")
        return html
    
    # 找到extra数组的起始位置
    extra_start = html.find('"extra": [', pos)
    if extra_start < 0:
        print(f"未找到extra字段: {province_name}")
        return html
    
    # 找到extra数组的结束位置
    questions_pos = html.find('"questions": [', extra_start)
    if questions_pos < 0:
        print(f"未找到questions字段: {province_name}")
        return html
    
    # 计算extra结束位置（向前找到]）
    i = questions_pos - 1
    while i > extra_start and html[i] in ' \t\n':
        i -= 1
    extra_end = i  # 此时html[i]是]
    
    # 生成新的extra数组
    new_extra = generate_extra_section(items1, items2, items3, items4, items5)
    
    # 替换
    new_html = html[:extra_start] + f'"extra": {new_extra},\n    "questions"' + html[questions_pos + len('"questions"'):]
    
    return new_html

