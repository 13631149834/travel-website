def esc(s):
    """转义单引号和反斜杠"""
    return s.replace('\\', '\\\\').replace("'", "\\'").replace('\n', ' ')

def find_block_start(html, province_name):
    """找到省份数据块的起始位置"""
    # 查找 "省份名": { 或 "省份名":{
    patterns = [
        f'"{province_name}": {{',
        f'"{province_name}":{{'
    ]
    for p in patterns:
        pos = html.find(p)
        if pos != -1:
            return pos
    return -1

def find_block_end(html, start):
    """找到省份数据块的结束位置(匹配的花括号)"""
    brace_count = 0
    in_str = False
    str_char = None
    i = start
    
    while i < len(html):
        c = html[i]
        if in_str:
            if c == str_char:
                # 检查是否是转义的引号
                if i == 0 or html[i-1] != '\\':
                    in_str = False
        else:
            if c in ("'", '"'):
                in_str = True
                str_char = c
            elif c == '{':
                brace_count += 1
            elif c == '}':
                brace_count -= 1
                if brace_count == 0:
                    return i + 1
        i += 1
    return len(html)

