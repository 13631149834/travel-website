-- 如果表已存在则删除（用于重新初始化）
DROP TABLE IF EXISTS `route_departures`;
DROP TABLE IF EXISTS `route_itinerary`;
DROP TABLE IF EXISTS `route_includes`;
DROP TABLE IF EXISTS `tour_routes`;

-- ============================================
-- 1. 路线主表
-- ============================================
CREATE TABLE `tour_routes` (
    `id` INT PRIMARY KEY AUTO_INCREMENT COMMENT '路线ID',
    `name` VARCHAR(200) NOT NULL COMMENT '路线名称',
    `subtitle` VARCHAR(300) DEFAULT NULL COMMENT '副标题',
    `destination` VARCHAR(500) NOT NULL COMMENT '目的地（逗号分隔）',
    `country` VARCHAR(100) NOT NULL COMMENT '国家/地区',
    `days` INT NOT NULL COMMENT '行程天数',
    `min_price` DECIMAL(10,2) NOT NULL COMMENT '最低价格（元/人）',
    `max_price` DECIMAL(10,2) NOT NULL COMMENT '最高价格（元/人）',
    `highlights` TEXT COMMENT '行程亮点（JSON数组）',
    `featured_image` VARCHAR(500) DEFAULT NULL COMMENT '封面图片URL',
    `overview` TEXT COMMENT '路线概述',
    `best_season` VARCHAR(200) DEFAULT NULL COMMENT '最佳季节',
    `guide_id` INT DEFAULT 1 COMMENT '负责导游ID',
    `status` TINYINT DEFAULT 1 COMMENT '状态：0-下架 1-上架 2-售罄',
    `created_at` DATETIME DEFAULT CURRENT_TIMESTAMP,
    `updated_at` DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='旅游路线主表';

-- ============================================
-- 2. 行程安排表
-- ============================================
CREATE TABLE `route_itinerary` (
    `id` INT PRIMARY KEY AUTO_INCREMENT,
    `route_id` INT NOT NULL COMMENT '路线ID',
    `day_number` INT NOT NULL COMMENT '第几天',
    `city` VARCHAR(100) NOT NULL COMMENT '所在城市',
    `title` VARCHAR(200) NOT NULL COMMENT '行程标题',
    `morning` TEXT COMMENT '上午安排',
    `afternoon` TEXT COMMENT '下午安排',
    `evening` TEXT COMMENT '晚上安排',
    `meal` VARCHAR(50) DEFAULT NULL COMMENT '用餐安排（如：含早/含三餐）',
    `hotel` VARCHAR(200) DEFAULT NULL COMMENT '住宿安排',
    `tips` TEXT DEFAULT NULL COMMENT '温馨提示',
    `sort_order` INT DEFAULT 0,
    FOREIGN KEY (`route_id`) REFERENCES `tour_routes`(id) ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='行程安排表';

-- ============================================
-- 3. 费用说明表
-- ============================================
CREATE TABLE `route_includes` (
    `id` INT PRIMARY KEY AUTO_INCREMENT,
    `route_id` INT NOT NULL,
    `type` TINYINT NOT NULL COMMENT '类型：1-包含 2-不包含',
    `item` VARCHAR(200) NOT NULL COMMENT '费用项目',
    `sort_order` INT DEFAULT 0,
    FOREIGN KEY (`route_id`) REFERENCES `tour_routes`(id) ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='费用说明表';

-- ============================================
-- 4. 出发日期表
-- ============================================
CREATE TABLE `route_departures` (
    `id` INT PRIMARY KEY AUTO_INCREMENT,
    `route_id` INT NOT NULL,
    `departure_date` DATE NOT NULL COMMENT '出发日期',
    `return_date` DATE NOT NULL COMMENT '返回日期',
    `remaining_seats` INT DEFAULT 20 COMMENT '剩余座位',
    `status` TINYINT DEFAULT 1 COMMENT '状态：0-不可选 1-可选 2-已成团',
    FOREIGN KEY (`route_id`) REFERENCES `tour_routes`(id) ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='出发日期表';

