def replace_extra_array(content, province_name, new_data):
    province_pos = content.find(province_name + ":{")
    if province_pos == -1:
        print(f"未找到 {province_name}")
        return content
    
    extra_start = content.find("extra:[", province_pos)
    if extra_start == -1:
        print(f"未找到 {province_name} extra数组")
        return content
    
    questions_pos = content.find("],\nquestions:", extra_start)
    if questions_pos == -1:
        print(f"未找到 {province_name} questions")
        return content
    
    extra_end = content.find("]", extra_start)
    while extra_end < questions_pos:
        next_bracket = content.find("]", extra_end + 1)
        if next_bracket == -1 or next_bracket > questions_pos:
            break
        extra_end = next_bracket
    
    print(f"找到 {province_name} extra: {extra_start} - {extra_end}")
    new_extra = new_data
    content = content[:extra_start + 7] + "\n" + new_extra + "\n" + content[extra_end:]
    return content

