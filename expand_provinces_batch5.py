def esc(s):
    """转义字符串用于JS单引号格式"""
    return s.replace('\\', '\\\\').replace("'", "\\'").replace('\n', ' ').replace('\r', ' ').replace('"', '\\"')

def find_block_end(html, start):
    """找到JS数据块结束位置"""
    brace_count = 0
    in_str = False
    str_char = None
    i = start
    while i < len(html):
        c = html[i]
        if in_str:
            if c == str_char and (i == 0 or html[i-1] != '\\'):
                in_str = False
        else:
            if c in ("'", '"'):
                in_str = True
                str_char = c
            elif c == '{':
                brace_count += 1
            elif c == '}':
                brace_count -= 1
                if brace_count == 0:
                    return i + 1
        i += 1
    return len(html)

def count_chinese_chars(text):
    """统计中文字符数"""
    return len(re.findall(r'[\u4e00-\u9fff]', text))

