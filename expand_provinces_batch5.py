def generate_extra_block(extra_data):
    """生成extra数组的JS代码"""
    items_list = []
    for section in extra_data:
        section_items = []
        for item in section['items']:
            section_items.append("'" + esc(item) + "'")
        section_str = "{'title':'" + esc(section['title']) + "','items':[" + ','.join(section_items) + "]}"
        items_list.append(section_str)
    return '[' + ','.join(items_list) + ']'

def generate_questions_block(questions):
    """生成questions数组的JS代码"""
    q_list = []
    for q in questions:
        opts = ','.join(["'" + esc(o) + "'" for o in q['options']])
        q_str = "{'q':'" + esc(q['q']) + "','options':[" + opts + "],'answer':" + str(q['answer']) + ",'explain':'" + esc(q['explain']) + "'}"
        q_list.append(q_str)
    return '[' + ','.join(q_list) + ']'

