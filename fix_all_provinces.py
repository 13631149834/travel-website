import re

def count_text(text):
    """去除HTML标签后，只保留中英文数字，计算纯文本字数"""
    text = re.sub(r'<[^>]+>', '', text)
    text = re.sub(r'[^\u4e00-\u9fa5a-zA-Z0-9]', '', text)
    return len(text)

def escape_js_str(s):
    """转义JavaScript字符串中的特殊字符"""
    return s.replace("\\", "\\\\").replace("'", "\\'").replace("\n", "\\n")

