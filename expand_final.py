import re

def count_chinese(text):
    return len(re.findall(r'[\u4e00-\u9fff]', text))

def count_total_chinese(extra_content):
    items = re.findall(r'items:\[(.*?)\]', extra_content, re.DOTALL)
    total = 0
    for item_list in items:
        texts = re.findall(r"'([^']*)'", item_list)
        for text in texts:
            total += count_chinese(text)
    return total

