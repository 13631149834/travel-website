def replace_province_block(html, province_name, new_extra, new_questions, region):
    pattern = province_name + ":{\nregion:"
    start = html.find(pattern)
    if start == -1:
        print("Error: " + province_name + " block not found")
        return html, False
    
    official_start = html.find("official:", start)
    official_end = html.find("},", official_start)
    official_block = html[official_start:official_end+2]
    
    extra_pos = html.find("extra:[", start)
    questions_pos = html.find("questions:[", extra_pos)
    
    search_start = questions_pos
    while True:
        end_pos = html.find("},", search_start)
        if end_pos == -1:
            break
        next_content = html[end_pos+2:end_pos+20].strip()
        provinces = ["北京", "天津", "河北", "山西", "内蒙古", "辽宁", "吉林", "黑龙江", "上海", "江苏", "浙江", "安徽", "福建", "江西", "山东", "湖南", "广东", "广西", "重庆", "四川", "贵州", "云南", "西藏", "陕西", "甘肃", "青海", "宁夏", "新疆"]
        if any(next_content.startswith(p + ":") for p in provinces):
            block_end = end_pos + 2
            break
        search_start = end_pos + 2
    else:
        block_end = end_pos + 2 if end_pos != -1 else len(html)
    
    new_block = province_name + ":{\nregion:'" + region + "',\n" + official_block + "\nextra:" + new_extra + ",\nquestions:" + new_questions + "\n}}"
    html = html[:start] + new_block + html[block_end:]
    return html, True

